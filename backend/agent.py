_agent_executor = None  # the compiled agent (langgraph graph or AgentExecutor)


def _make_executor(model: str = "claude-haiku-4-5-20251001", user_context: dict = None):
    """Build the agent using Claude (Anthropic)."""
    api_key = os.getenv("ANTHROPIC_API_KEY", "")
    if not api_key:
        raise ValueError("ANTHROPIC_API_KEY not set in .env")
    llm = ChatAnthropic(
        model=model,
        anthropic_api_key=api_key,
        temperature=0,
        max_tokens=2048,
    )
    tools = [run_sql, make_chart]
    sys_prompt = _build_system_prompt(user_context)

    if _USE_LANGGRAPH:
        # langchain >=1.0: create_agent returns a CompiledStateGraph
        return create_agent(model=llm, tools=tools, system_prompt=sys_prompt)
    else:
        # langchain <1.0: legacy AgentExecutor
        prompt = ChatPromptTemplate.from_messages([
            ("system", sys_prompt),
            MessagesPlaceholder("chat_history", optional=True),
            ("human", "{input}"),
            ("placeholder", "{agent_scratchpad}"),
        ])
        agent = create_tool_calling_agent(llm, tools, prompt)
        return AgentExecutor(agent=agent, tools=tools, verbose=True, max_iterations=8)


def _build_agent(user_context: dict = None):
    return _make_executor(user_context=user_context)


