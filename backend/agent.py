"""
agent.py — LangChain SQL Agent backed by Anthropic Claude.

The agent has two tools:
  - run_sql(query)            → executes SQL on transactions.db, returns JSON rows
  - make_chart(type, sql, title) → runs SQL, builds a Plotly figure, stored
                                   in _pending_chart so the caller can retrieve it

Public API:
    from agent import query_agent
    result = query_agent("how much did I spend in January?")
    # result = {"text": str, "chart": plotly.Figure | None, "suggestions": list[str]}
"""

import os
import sqlite3
from pathlib import Path

import pandas as pd
