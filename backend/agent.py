@tool
def run_sql(query: str) -> str:
    """
    Execute a SQL SELECT query on the transactions database and return results as JSON.

    Table: transactions
    Columns:
      transaction_id, account_id, merchant_name, merchant_id,
      timestamp (TEXT, format YYYY-MM-DD HH:MM:SS),
      transaction_description, category, amount (REAL, EUR),
      currency, predicted_category, confidence, correct,
      year (INT), month (INT 1-12), day (INT)

    Categories: Food & Dining, Transportation, Entertainment & Recreation,
    Shopping & Retail, Financial Services, Healthcare & Medical,
    Utilities & Services, Income, Government & Legal, Charity & Donations

    To filter expenses only: WHERE predicted_category != 'Income'
    """
    try:
        conn = sqlite3.connect(DB_PATH)
        df = pd.read_sql_query(query, conn)
        conn.close()
        if df.empty:
            return "No results found for this query."
        return df.head(100).to_json(orient="records", date_format="iso")
    except Exception as e:
        return f"SQL Error: {e}"


@tool
def make_chart(chart_type: str, sql_query: str, title: str = "") -> str:
    """
    Create a Plotly chart from a SQL query and store it for the frontend.

    chart_type : 'bar' | 'line' | 'donut'
    sql_query  : SQL that returns exactly two columns → (label/x, value/y)
    title      : chart title shown to the user

    Use this whenever the user asks for a graph, chart, or when showing
    trends over time or category breakdowns would be helpful.
    """
    try:
        conn = sqlite3.connect(DB_PATH)
        df = pd.read_sql_query(sql_query, conn)
        conn.close()

        if df.empty:
            return "No data to chart."

        cols = df.columns.tolist()
        x_col = cols[0]
        y_col = cols[1] if len(cols) > 1 else cols[0]

        # Keep only the two relevant columns, then aggregate duplicate labels
        df = df[[x_col, y_col]].copy()
        if df[x_col].duplicated().any():
            df = df.groupby(x_col, sort=False)[y_col].sum().reset_index()

        # Convert month numbers → short names for readability
        MONTH_NAMES = {1:"Jan",2:"Feb",3:"Mar",4:"Apr",5:"May",6:"Jun",
                       7:"Jul",8:"Aug",9:"Sep",10:"Oct",11:"Nov",12:"Dec"}
        if df[x_col].dtype in ["int64","float64"]:
            unique_vals = df[x_col].dropna().unique()
            if set(unique_vals).issubset(set(range(1, 13))):
                df = df.copy()
                df[x_col] = df[x_col].map(lambda m: MONTH_NAMES.get(int(m), str(m)))

        BG   = "#FAFAF8"
        GRID = "#EDEBE5"

        LAYOUT = dict(
            height=340,
            paper_bgcolor=BG,
            plot_bgcolor=BG,
            font=dict(family="DM Sans", size=13, color="#1A1916"),
            margin=dict(t=48, b=72, l=64, r=24),
            title=dict(
                text=f"<b>{title}</b>",
                font=dict(family="DM Sans", size=14, color="#1A1916"),
                x=0, xanchor="left",
            ),
            hoverlabel=dict(
                bgcolor="#1A1916", font_color="#fff",
                font_family="DM Sans", font_size=12,
                bordercolor="#1A1916",
            ),
        )

        CAT_COLORS = {
            "Food & Dining":              "#E8860C",
            "Transportation":             "#2E6BC6",
            "Entertainment & Recreation": "#8B3AA0",
            "Shopping & Retail":          "#C4881D",
            "Financial Services":         "#888888",
            "Healthcare & Medical":       "#D94444",
            "Utilities & Services":       "#3A6A9B",
            "Income":                     "#2A9D6A",
            "Government & Legal":         "#5A6070",
            "Charity & Donations":        "#C44A7A",
        }

        MONTH_SET = {"Jan","Feb","Mar","Apr","May","Jun",
                     "Jul","Aug","Sep","Oct","Nov","Dec"}

        bar_colors = [CAT_COLORS.get(str(lbl), None) for lbl in df[x_col]]
        has_cat_colors = any(c is not None for c in bar_colors)

        # Detect time-series: labels are months, years, or year-month strings
        labels_str = [str(l) for l in df[x_col]]
        is_time_series = (
            set(labels_str).issubset(MONTH_SET) or
            all(str(l).isdigit() and (1900 < int(l) < 2100) for l in labels_str) or
            all("-" in str(l) or "/" in str(l) for l in labels_str)
        )

        if has_cat_colors:
            bar_colors = [c or "#AAAAAA" for c in bar_colors]
        elif is_time_series:
            # Single consistent color for time-based charts
            bar_colors = ["#2E6BC6"] * len(df)
        else:
            # Distinct colors for non-category, non-time data (e.g. merchants)
            PALETTE = ["#1A1916","#E8860C","#2E6BC6","#8B3AA0",
                       "#C4881D","#D94444","#3A6A9B","#2A9D6A"]
            bar_colors = [PALETTE[i % len(PALETTE)] for i in range(len(df))]

        if chart_type == "bar":
            # Use horizontal bars when labels are long (categories) or many items
            avg_label_len = sum(len(str(l)) for l in df[x_col]) / max(len(df), 1)
            use_horizontal = avg_label_len > 8 or len(df) > 5

            if use_horizontal:
                # Sort ascending so biggest is at top
                df_sorted = df.sort_values(y_col, ascending=True)
                h_colors = [CAT_COLORS.get(str(lbl), None) for lbl in df_sorted[x_col]]
                h_colors = [c or PALETTE[i % len(PALETTE)] for i, c in enumerate(h_colors)]
                fig = go.Figure(data=[go.Bar(
                    y=df_sorted[x_col], x=df_sorted[y_col],
                    orientation="h",
                    marker_color=h_colors,
                    marker_line_width=0,
                    text=[f"€{v:,.0f}" for v in df_sorted[y_col]],
                    textposition="outside",
                    textfont=dict(size=11, color="#1A1916", family="DM Sans"),
                    cliponaxis=False,
                    hovertemplate="<b>%{y}</b><br>€%{x:,.0f}<extra></extra>",
                )])
                fig.update_layout(**LAYOUT)
                max_val = df_sorted[y_col].max()
                fig.update_layout(
                    height=max(300, len(df) * 48 + 90),
                    margin=dict(t=48, b=24, l=170, r=100),
                    xaxis=dict(
                        showgrid=True, gridcolor=GRID, gridwidth=1,
                        zeroline=False, showline=False,
                        tickprefix="€", tickformat=",.0f",
                        tickfont=dict(size=12, color="#1A1916"),
                        range=[0, max_val * 1.40],
                    ),
                    yaxis=dict(
                        showgrid=False, showline=False,
                        tickfont=dict(size=12, color="#1A1916"),
                        automargin=True,
                    ),
                    bargap=0.28,
                )
            else:
                fig = go.Figure(data=[go.Bar(
                    x=df[x_col], y=df[y_col],
                    marker_color=bar_colors,
                    marker_line_width=0,
                    text=[f"€{v:,.0f}" for v in df[y_col]],
                    textposition="outside",
                    textfont=dict(size=11, color="#1A1916", family="DM Sans"),
                    hovertemplate="<b>%{x}</b><br>€%{y:,.0f}<extra></extra>",
                )])
                fig.update_layout(**LAYOUT)
                max_val = df[y_col].max()
                fig.update_layout(
                    xaxis=dict(
                        showgrid=False, showline=False,
                        tickangle=-35, automargin=True,
                        tickfont=dict(size=12, color="#1A1916"),
                    ),
                    yaxis=dict(
                        showgrid=True, gridcolor=GRID, gridwidth=1,
                        zeroline=False, showline=False,
                        tickprefix="€", tickformat=",.0f",
                        tickfont=dict(size=12, color="#1A1916"),
                        range=[0, max_val * 1.20],
                    ),
                    bargap=0.35,
                )

        elif chart_type == "line":
            fig = go.Figure()
            show_labels = len(df) <= 14
            fig.add_trace(go.Scatter(
                x=df[x_col], y=df[y_col],
                mode="lines+markers+text" if show_labels else "lines+markers",
                line=dict(color="#1A1916", width=2.5, shape="spline", smoothing=0.4),
                marker=dict(size=5 if not show_labels else 7, color="#1A1916",
                            line=dict(color=BG, width=2)),
                fill="tozeroy",
                fillcolor="rgba(26,25,22,0.06)",
                **(dict(
                    text=[f"€{v:,.0f}" for v in df[y_col]],
                    textposition="top center",
                    textfont=dict(size=10, color="#1A1916"),
                ) if show_labels else {}),
                hovertemplate="<b>%{x}</b><br>€%{y:,.0f}<extra></extra>",
            ))
            fig.update_layout(**LAYOUT)
            fig.update_layout(
                xaxis=dict(
                    showgrid=False, showline=False,
                    tickangle=-35, automargin=True,
                    tickfont=dict(size=12, color="#1A1916"),
                ),
                yaxis=dict(
                    showgrid=True, gridcolor=GRID, gridwidth=1,
                    zeroline=False, showline=False,
                    tickprefix="€", tickformat=",.0f",
                    tickfont=dict(size=12, color="#1A1916"),
                ),
            )

        elif chart_type == "donut":
            DONUT_PALETTE = [
                "#1A1916", "#E8860C", "#2E6BC6", "#8B3AA0",
                "#C4881D", "#D94444", "#3A6A9B", "#2A9D6A",
                "#C44A7A", "#5A6070", "#F0A500", "#4A90D9",
            ]
            raw_colors = [CAT_COLORS.get(str(lbl), None) for lbl in df[x_col]]
            has_any = any(c is not None for c in raw_colors)
            if has_any:
                colors = [c or "#AAAAAA" for c in raw_colors]
            else:
                # Non-category labels (merchants, etc.) → use distinct palette
                colors = [DONUT_PALETTE[i % len(DONUT_PALETTE)] for i in range(len(df))]
            total = df[y_col].sum()
            fig = go.Figure(data=[go.Pie(
                labels=df[x_col],
                values=df[y_col],
                hole=0.58,
                marker_colors=colors,
                marker_line=dict(color=BG, width=2),
                textinfo="percent",
                textfont=dict(size=11, family="DM Sans"),
                hovertemplate="<b>%{label}</b><br>€%{value:,.0f}<br>%{percent}<extra></extra>",
                sort=True,
            )])
            fig.update_layout(**LAYOUT)
            fig.update_layout(
                margin=dict(t=48, b=16, l=16, r=16),
                showlegend=True,
                legend=dict(
                    orientation="v", x=1.02, y=0.5,
                    font=dict(size=11, family="DM Sans", color="#1A1916"),
                    itemsizing="constant",
                ),
                annotations=[dict(
                    text=f"<b>€{total:,.0f}</b>",
                    x=0.5, y=0.5, showarrow=False,
                    font=dict(size=15, family="DM Sans", color="#1A1916"),
                    xanchor="center",
                )],
            )

        else:
            fig = go.Figure(data=[go.Bar(
                x=df[x_col], y=df[y_col],
                marker_color="#1A1916", marker_line_width=0,
            )])
            fig.update_layout(**LAYOUT)

        _pending_chart["figs"].append(fig)
        return f"Chart '{title}' created successfully."

    except Exception as e:
        return f"Chart error: {e}"


