# HELPERS
# ─────────────────────────────────────────────────────────────────────────────

def _extract_text(output) -> str:
    """
    Claude (via langchain-anthropic) sometimes returns output as:
      - a plain string  → use as-is
      - a list of content blocks: [{'type':'text','text':'...','index':0}]
      - a stringified version of the above
    This helper normalises all cases to a plain string.
    """
    if not output:
        return "Sorry, I couldn't find an answer."

    # Already a clean string
    if isinstance(output, str):
        # Check if it looks like a stringified list of content blocks
        stripped = output.strip()
        if stripped.startswith("[{") or stripped.startswith("[{'"):
            try:
                import ast
                blocks = ast.literal_eval(stripped)
                if isinstance(blocks, list):
                    parts = [b.get("text", "") for b in blocks if isinstance(b, dict) and b.get("type") == "text"]
                    joined = "\n".join(parts).strip()
                    if joined:
                        return joined
            except Exception:
                pass
        return output

    # List of content block dicts
    if isinstance(output, list):
        parts = [b.get("text", "") for b in output if isinstance(b, dict) and b.get("type") == "text"]
        joined = "\n".join(parts).strip()
        return joined if joined else str(output)

    return str(output)


