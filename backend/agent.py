# langchain 1.x uses create_agent (langgraph-based);
# fall back to legacy AgentExecutor for langchain 0.x
try:
    from langchain.agents import create_agent          # langchain >=1.0
    _USE_LANGGRAPH = True
except ImportError:
    from langchain.agents import AgentExecutor, create_tool_calling_agent  # langchain <1.0
    from langchain_core.prompts import ChatPromptTemplate, MessagesPlaceholder
    _USE_LANGGRAPH = False

# Load .env from the backend directory (works from any working directory)
_BACKEND_DIR = Path(__file__).resolve().parent
load_dotenv(_BACKEND_DIR / ".env", override=True)

DB_PATH = str(_BACKEND_DIR / "data" / "transactions.db")

# Stores the last chart generated by the make_chart tool (side-effect pattern)
_pending_chart: dict = {"figs": []}


